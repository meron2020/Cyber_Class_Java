package Grade_11.DataStructures.Node.BookQuestions.Page_99.Q49;

public class TavAndNum {
    private int num;
    private char tav;

    public TavAndNum(int num, char tav) {
        this.num = num;
        this.tav = tav;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public char getTav() {
        return tav;
    }

    public void setTav(char tav) {
        this.tav = tav;
    }
}
