package Grade_11.StudiesForTests.Matkonet.Question2;

public class KoronaCity {
    String name;
    int numP;

    public KoronaCity(String name, int numP) {
        this.name = name;
        this.numP = numP;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumP() {
        return numP;
    }

    public void setNumP(int numP) {
        this.numP = numP;
    }
}
